const messages = { ru, kk, en }
const browserLocales = { ru: 'ru-RU', kk: 'kk-KZ', en: 'en-US' }

function initialLocale() {
  try {
    const stored = window.localStorage.getItem(STORAGE_KEY)
    if (SUPPORTED_LOCALES.includes(stored)) return stored
  } catch {
    // Local storage can be disabled; browser language remains a safe fallback.
  }
  const browserLocale = window.navigator.language?.slice(0, 2).toLowerCase()
  return SUPPORTED_LOCALES.includes(browserLocale) ? browserLocale : 'ru'
}

function interpolate(template, values) {
  return String(template).replace(/\{\{(\w+)\}\}/g, (_, key) => values[key] ?? '')
}

const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [locale, setLocaleState] = useState(initialLocale)

  const setLocale = useCallback((nextLocale) => {
    const safeLocale = SUPPORTED_LOCALES.includes(nextLocale) ? nextLocale : 'ru'
    setLocaleState(safeLocale)
    try { window.localStorage.setItem(STORAGE_KEY, safeLocale) } catch { /* no-op */ }
  }, [])

  useEffect(() => {
    document.documentElement.lang = locale === 'kk' ? 'kk' : locale
  }, [locale])

  const t = useCallback((key, values = {}) => {
    const template = messages[locale]?.[key] ?? ru[key] ?? key
    return interpolate(template, values)
  }, [locale])

  const value = useMemo(() => ({
    locale,
    localeTag: browserLocales[locale],
    setLocale,
    t,
    formatNumber: (number, options) => new Intl.NumberFormat(browserLocales[locale], options).format(number),
    formatDate: (date, options) => new Intl.DateTimeFormat(browserLocales[locale], options).format(date),
    periodLabel: (period) => {
      const year = String(period?.period_id || period || '').match(/^\d{4}/)?.[0]
      return year ? t('season.summer', { year }) : (period?.label || String(period || ''))
    },
  }), [locale, setLocale, t])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const value = useContext(I18nContext)
  if (!value) throw new Error('useI18n must be used inside I18nProvider')
  return value
}
